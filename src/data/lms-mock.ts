export type ContentType = 'article' | 'video' | 'script' | 'quiz' | 'presentation' | 'pdf'
export type TargetRole = 'all' | 'manager' | 'rop' | 'director'

export interface ArticleContent { type: 'article'; body: string }
export interface VideoContent { type: 'video'; url: string; description?: string }
export interface ScriptContent {
  type: 'script'
  lines: Array<{ speaker: 'manager' | 'client'; text: string }>
}
export interface QuizContent {
  type: 'quiz'
  questions: Array<{ question: string; options: string[]; correct: number }>
}

export interface PresentationContent {
  type: 'presentation'
  slides: Array<{ title: string; body: string }>
}

export interface PdfContent {
  type: 'pdf'
  url: string
  description?: string
}

export interface LMSItem {
  id: string
  type: ContentType
  title: string
  description: string
  targetRole: TargetRole
  readTime?: string
  tags?: string[]
  content: ArticleContent | VideoContent | ScriptContent | QuizContent | PresentationContent | PdfContent
}

// ─── Курсовая структура ────────────────────────────────────────────────────────

export interface LMSLesson {
  id: string
  title: string
  type: 'article' | 'script' | 'quiz'
  duration: string
  body?: string   // Для article/script — текст содержимого
  quiz?: Array<{ question: string; options: string[]; correct: number }> // Для quiz
}

export interface LMSCourse {
  id: string
  title: string
  description: string
  targetRoles: TargetRole[]
  emoji: string
  lessons: LMSLesson[]
  passingScore: number  // % для финального теста
}

